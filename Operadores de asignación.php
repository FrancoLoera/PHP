<?php
// OPERADORES DE ASIGNACIÓN
# Asignación
$var = 1;
echo $var;

# Sumar y asignar
$var += 1;
echo "<br>" . $var;

# Restar y asignar
$var -= 1;
echo "<br>" . $var;

# Multiplicar y asignar
$var *= 2;
echo "<br>" . $var;

# Dividir y asignar
$var /= 2;
echo "<br>" . $var;

# Concatenar y asignar
$nombre = "Franco";
$nombre .= " Loera";
echo "<br>" . $nombre;
?>