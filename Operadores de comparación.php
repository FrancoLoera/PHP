<?php
// OPERADORES DE COMPARACIÓN
# Comparación sin exclusión de tipos de datos
# Igual que
echo var_dump(1 == "1");

# Distinto de
echo var_dump(1 != 0);
echo var_dump(1 <> 0);

# Menor que
echo var_dump(7 < 4);

# Mayor que
echo var_dump(6 > 2);

# Menor igual que
echo var_dump(7 < 4);

# Mayor igual que
echo var_dump(6 > 2);

# Comparación con exclusión de tipos de datos (evalúa si es el mismo tipo de dato)
# Idéntico que
echo var_dump(1 === "1");

# No identico que
echo var_dump(5 !== "15");

?>